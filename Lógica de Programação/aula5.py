# Tipo de dado bool (booleano)
# Ao questionar algo em um programa, só existem duas respostas possíveis: sim(True) ou não(False)
# Existem vários operadores para "questionar", dentr eles o ==, que é um operador lógico que questiona
# se um valor é igual a outro
print(10 == 10) # Sim => True(Verdadeiro)
print(10 == 11) # Não => False(Falso)
print(type(10 == 10)) # bool
print(type(10 == 11)) # bool    
print(type(True)) # bool
print(type(False)) # bool