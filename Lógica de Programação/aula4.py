# Tipos int e float
# int -> Número inteiro
# O tipo int representa qualquer número inteiro positivo ou negativo
print(10) # int
print(-10) # int
print(0) # int
print()

# float -> Número com ponto flutuante
# O tipo float representa qualquer número com ponto flutuante positivo ou negativo
print(10.5) # float
print(-10.5) # float
print(0.0) # float
print()

# A função type mostra  o tipo que o Python inferiu ao valor
print(type(10)) # int
print(type(-10)) # int
print(type(0)) # int
print()
print(type(10.5)) # float
print(type(-10.5)) # float
print(type(0.0)) # float